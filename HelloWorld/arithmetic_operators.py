print(10 + 3)       # Addition
print(10 - 3)       # Subtraction
print(10 * 3)       # Multiplication
print(10 / 3)       # Yields Floating Quotient
print(10 // 3)      # Yields Int Quotient
print(10 % 3)       # Modulo
print(10 ** 3)      # Power

x = 10
x += 3              # Augmented Addition
print(x)

a = 10 + 3 * 2 ** 2 
print(a)

b = (2 + 3) * 10 - 3
print(b)